#include "libft.h"

void				ft_remove_char(char *cmd, char c)
{
	int				i;
	int				j;

	i = 0;
	j = 0;
	if (cmd)
	{
		while (cmd[i])
		{
			while (cmd[j] == c)
				j++;
			cmd[i] = cmd[j];
			i++;
			j++;
		}
		cmd[i] = '\0';
	}
}

#include "libft.h"

void			ft_bzero(void *s, size_t n)
{
	size_t		i;
	char		*bkp;

	bkp = s;
	i = 0;
	if (n > 0)
	{
		while (i < n)
			bkp[i++] = 0;
	}
}
